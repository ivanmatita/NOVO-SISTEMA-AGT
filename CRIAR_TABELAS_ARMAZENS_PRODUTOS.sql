        WHERE id = auth.uid()
        LIMIT 1
    )
)
WITH CHECK (
    empresa_id = (
        SELECT empresa_id FROM public.perfis
        WHERE id = auth.uid()
        LIMIT 1
    )
);

-- Activar Realtime
DO $$
BEGIN
